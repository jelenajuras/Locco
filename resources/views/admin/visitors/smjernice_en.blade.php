VISITORS SAFETY INSTRUCTIONS

Visitors are obliged to adhere to the following: 
• all provisions of the Occupational Safety and Health Act and the General Regulations for Safe Operation
• observe warning and danger signs
• follow the instructions from the host
• dispose of waste exclusively in the containers provided, while paying attention to the separation of waste by type, if possible.
Iti s forbidden:
• access to places where no visit is planned
• standing near equipment and machines in operation, unattended
• smoking on company premises,, bringing and consumption alcohol, drugs and other intoxicants
• shooting and recording without special permission

Failure to follow these instructions can lead to potential accidents and injuries and a ban on entering the company.

In the event of any emergency, stay calm and follow the instructions given by Duplico employees and follow the evacuation plans below if you need to evacuate.    
