   SICHERHEITSHINWEISE FÜR BESUCHER
   
   Besucher sind verpflichtet, Folgendes zu beachten:
   • Alle Bestimmungen des Arbeitsschutzgesetzes und der Allgemeinen Vorschriften für arbeiten in sicherer Umgebung
   • Warnschilder, Warnungen und Gefahren beachten
   • Befolgen Sie alle Anweisungen des Gastgebers
   • Entsorgen Sie die Abfälle ausschließlich in den dafür vorgesehenen Behältern und achten Sie nach Möglichkeit auf die sortenreine Trennung der Abfälle.
   Es ist untersagt:
   • Zugang zu Orten, an denen kein Besuch geplant ist
   • Unbeaufsichtigt in Arbeitszonen mit maschinen unterwegs zu sein
   • Rauchen auf dem Firmengelände, Betreten und Konsumieren von Alkohol und anderen Rauschmitteln
   • Foto und Videoaufnahmen ohne besondere Erlaubnis
   Die Nichtbeachtung der obigen Anweisungen kann zu möglichen Unfällen und Verletzungen sowie zu einem Verbot des Eintritts in das Unternehmen führen.
   Bleiben Sie im Notfall ruhig und befolgen Sie die Anweisungen der Mitarbeiter von Duplico sowie die unten aufgeführten Evakuierungspläne, wenn Sie evakuieren müssen. 
